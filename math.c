#include <stdio.h>
#include <math.h>

int main(int argc, char const *argv[])
{
  printf("%lf\n", exp(2));
  printf("%lf\n", log10(10));
  printf("%lf\n", pow(5, 5));
  printf("%lf\n", sqrt(25));
  printf("%lf\n", ceil(5.4));
  printf("%lf\n", floor(5.4));
  printf("%lf\n", fabs(-10.45));

  return 0;
}
